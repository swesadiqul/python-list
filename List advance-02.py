# Updating and Deleting List Elements
# There are three function accessing of list elements
# Such as: append(),insert(),extend()

# Update element of list
# Append function
nums = [2,3,4,5,8,9,10]
nums.append(7)
print(nums)

# insert function
names = ["Sadiqul","Rony","Mahbub","Liton","Rejaul"]
names.insert(2,"Rabbi")
print(names)

# extend function
float_list = [12.3,5.6,2.12,6.2,8.5]
float_list.extend([4.12,23.5,10.2])
print(float_list)

# Delete item of list
# del function
mix_list = ["Rayhan","Bappi","Rasel","Rana","Mustafijur",2,3,4,7,8,7.2,12.5,4.7]
del mix_list[1]
print(mix_list)


# remove function
list1 = ["Rubayet","Milon","Raisul","Junayed","Lulfer"]
list1.remove("Raisul")
print(list1)


